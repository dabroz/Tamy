#include "core-AI\AnimationPlayer.h"


///////////////////////////////////////////////////////////////////////////////

BEGIN_ABSTRACT_OBJECT( AnimationPlayer );
   PARENT( Component );
END_OBJECT();

///////////////////////////////////////////////////////////////////////////////

AnimationPlayer::AnimationPlayer( const char* name )
   : Component( name )
   , m_isPlaying( false )
{
}

///////////////////////////////////////////////////////////////////////////////

AnimationPlayer::AnimationPlayer( const AnimationPlayer& rhs )
   : Component( rhs )
   , m_isPlaying( false )
{
}

///////////////////////////////////////////////////////////////////////////////

AnimationPlayer::~AnimationPlayer()
{
}

///////////////////////////////////////////////////////////////////////////////

void AnimationPlayer::start()
{
   m_isPlaying = true;

   onStarted();
}

///////////////////////////////////////////////////////////////////////////////

void AnimationPlayer::stop()
{
   onFinished();
   m_isPlaying = false;
}

///////////////////////////////////////////////////////////////////////////////
