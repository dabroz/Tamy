#include "core-Renderer\Font.h"


///////////////////////////////////////////////////////////////////////////////

Font::Font()
{
   m_renderResource = RenderResource::instantiate< Font >( this );
}

///////////////////////////////////////////////////////////////////////////////

Font::~Font()
{
   m_renderResource->removeReference();
   m_renderResource = NULL;
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

RCDrawText::RCDrawText( const Font* font, const std::string& text, const RECT& overlaySize, TextJustification justification, const Color& color )
   : m_font( font )
   , m_text( text )
   , m_overlaySize( overlaySize )
   , m_justification( justification )
   , m_color( color )
{
}

