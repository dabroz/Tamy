#include "core\Point.h"
#include "core\Vector.h"
#include "core\InStream.h"
#include "core\OutStream.h"
#include "core\Algorithms.h"


///////////////////////////////////////////////////////////////////////////////

Point::Point( int _x, int _y ) 
   : x( _x )
   , y( _y ) 
{}

///////////////////////////////////////////////////////////////////////////////

Point::Point() 
   : x( 0 )
   , y( 0 ) 
{}

///////////////////////////////////////////////////////////////////////////////

Point::Point( const Vector& vec ) 
   : x( ( int ) vec[0] )
   , y( ( int ) vec[1] ) 
{}

///////////////////////////////////////////////////////////////////////////////

Point::Point( const Point& rhs )
   : x( rhs.x )
   , y( rhs.y )
{
}

///////////////////////////////////////////////////////////////////////////////

void Point::setAsDirection( const Point& pt )
{
   x = sign( pt.x );
   y = sign( pt.y );
}

///////////////////////////////////////////////////////////////////////////////

std::ostream& operator<<( std::ostream& stream, const Point& rhs )
{
   stream << "[" << rhs.x << ", " << rhs.y << "]";
   return stream;
}

///////////////////////////////////////////////////////////////////////////////

OutStream& operator<<( OutStream& stream, const Point& rhs )
{
   stream << rhs.x;
   stream << rhs.y;
   return stream;
}

///////////////////////////////////////////////////////////////////////////////

InStream& operator>>( InStream& stream, Point& rhs )
{
   stream >> rhs.x;
   stream >> rhs.y;
   return stream;
}

